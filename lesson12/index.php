<h2>3. Сделайте функцию, которая отнимает от первого числа второе и делит на третье.</h2>
<?php
function nz_todo3( $num1, $num2, $num3 ) {
	return ( $num1 - $num2 ) / $num3;
}

echo nz_todo3( 12, 6, 2 );
?>

