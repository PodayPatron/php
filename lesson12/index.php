<h2>2. Сделайте функцию, которая возвращает сумму двух чисел. Числа передаются параметрами функции. </h2>
<?php
function nz_todo2( $num1, $num2 ) {
	return $num1 + $num2;
}

echo nz_todo2( 12, 12 );
?>

