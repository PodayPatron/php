<h2>1.  Дан массив с числами. Проверьте, что в этом массиве есть число 5. 
Если есть - выведите 'да', а если нет - выведите 'нет'.</h2>

<?php
function nz_todo1() {
	$arr = array( 1, 2, 3, 4, 5, 6 );

	if ( in_array( 5, $arr ) ) {
		echo 'da';
	} else {
		echo 'net';
	}
}

