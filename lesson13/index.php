<h2>3. Дан массив с числами. Проверьте, есть ли в нем два одинаковых числа подряд.
Если есть - выведите 'да', а если нет - выведите 'нет'. </h2>

<?php
function nz_todo3() {
	$arr  = array( 1, 2, 33, 4, 66, 66, 36, 86, 35, 75 );
	$flag = false;

	for ( $i = 0; $i < count( $arr ); $i++ ) {
		if ( $arr[ $i ] === $arr[ ( $i - 1 ) ] ) {
			$flag = true;
		}
	}

	if ( $flag ) {
		echo 'da';
	} else {
		echo 'net';
	}

}

nz_todo3();
?>
