<h2>2.Дано число, например 31. Проверьте, что это число не делится ни на одно другое число кроме себя самого и единицы. 
То есть в нашем случае нужно проверить, что число 31 не делится на все числа от 2 до 30.
Если число не делится - выведите 'нет', а если делится - выведите 'да'.  </h2>

<?php
function nz_todo2() {
	$num  = 31;
	$true = false;

	for ( $i = 2; $i < $num; $i ++ ) {
		if ( 0 === $num % $i ) {
			$true = true;
		}
	}

	if ( $true === true ) {
		echo 'da';
	} else {
		echo 'net';
	}
}

nz_todo2();
?>

