<h2>3. Дано число. Сложите его цифры. Если сумма получилась более 9-ти,
опять сложите его цифры. И так, пока сумма не станет однозначным числом (9 и менее).</h2>
<?php
function nz_todo3( $num ) {
	$num = array_sum( str_split( $num ) );
	echo $num . '<br>';

	if ( 9 < $num ) {
		nz_todo3( $num );
	}
}

nz_todo3( 125 );
?>
