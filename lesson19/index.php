<h2>1.Сделайте функцию cut, которая первым параметром будет принимать строку,
а вторым параметром - сколько первых символов оставить в этой строке.
Второй параметр должен быть необязательным и по умолчанию принимать значение 10. </h2>
<?php
function nz_cut( $str, $num ) {
	return mb_substr( $str, 0, $num = 10 );
}

echo nz_cut( 'hello world!', 5 );
?>

