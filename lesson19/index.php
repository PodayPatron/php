<h2>2. Дан массив с числами. Выведите последовательно его элементы используя рекурсию и не используя цикл.</h2>
<?php
function nz_todo2( $arr ) {
	echo $arr[0] . '<br>';
	array_splice( $arr, 0, 1 );

	if ( ! empty( $arr ) ) {
		nz_todo2( $arr );
	}
}

nz_todo2( array( 1, 2, 3, 4, 5, 6 ) );
?>

