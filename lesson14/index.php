<h2>2. Сделайте функцию, которая параметрами принимает 2 числа. 
Если их сумма больше 10 - пусть функция вернет true, а если нет - false. </h2>
<?php
function nz_todo2( $num1, $num2 ) {
	if ( ( $num1 + $num2 ) > 10 ) {
		echo 'true';
	} else {
		echo 'false';
	}
}

nz_todo2( 10, 10 );
?>

