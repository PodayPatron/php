<h2>1.Сделайте функцию, которая параметрами принимает 2 числа. 
Если эти числа равны - пусть функция вернет true, а если не равны - false. </h2>
<?php
function nz_todo1( $num1, $num2 ) {
	if ( $num1 === $num2 ) {
		echo 'true';
	} else {
		echo 'false';
	}
}

nz_todo1( 10, 10 );
?>

